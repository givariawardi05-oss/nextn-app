"use client"

import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { VariantProps, cva } from "class-variance-authority"

import { useIsMobile } from "@/hooks/use-mobile"
import { cn } from "@/lib/utils"
import { Button } from "@/components/ui/button"
import { Separator } from "@/components/ui/separator"
import { Sheet, SheetContent, SheetHeader, SheetTitle, SheetDescription } from "@/components/ui/sheet"
import { TooltipProvider } from "@/components/ui/tooltip"

type SidebarContext = {
  open: boolean
  setOpen: (open: boolean) => void
}

const SidebarContext = React.createContext<SidebarContext | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }

  return context
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div">
>(
  (
    {
      className,
      children,
      ...props
    },
    ref
  ) => {
    const [open, setOpen] = React.useState(false)

    const contextValue = React.useMemo<SidebarContext>(
      () => ({
        open,
        setOpen,
      }),
      [open, setOpen]
    )

    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div
            className={cn(
              "group/sidebar-wrapper flex min-h-svh w-full",
              className
            )}
            ref={ref}
            {...props}
          >
            {children}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"


const Sidebar = React.forwardRef<
  HTMLDivElement,
  Omit<React.ComponentProps<typeof SheetContent>, 'side'>
>(
  ({
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { open, setOpen } = useSidebar()
    const isMobile = useIsMobile()

    if (isMobile) {
        return (
            <Sheet open={open} onOpenChange={setOpen}>
                <SheetContent
                    ref={ref}
                    side="left"
                    className={cn("w-64 bg-sidebar p-0 text-sidebar-foreground flex flex-col", className)}
                    {...props}
                >
                    <SheetHeader className='p-4 sr-only'>
                        <SheetTitle>Sidebar Menu</SheetTitle>
                        <SheetDescription>Navigation links for the application.</SheetDescription>
                    </SheetHeader>
                    {children}
                </SheetContent>
            </Sheet>
        )
    }

    return (
        <div
            ref={ref}
            className={cn(
                "fixed inset-y-0 left-0 z-40 flex-col bg-sidebar text-sidebar-foreground hidden lg:flex w-64",
                className)}
            {...props}
        >
            <div className="flex-1 overflow-hidden flex flex-col">
             {children}
            </div>
        </div>
    );
  }
)
Sidebar.displayName = "Sidebar"

